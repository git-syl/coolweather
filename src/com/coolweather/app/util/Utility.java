package com.coolweather.app.util;

import java.text.SimpleDateFormat;
import java.util.Date;
import java.util.Locale;

import org.json.JSONException;
import org.json.JSONObject;

import com.coolweather.app.db.CoolWeatherDB;
import com.coolweather.app.model.City;
import com.coolweather.app.model.County;
import com.coolweather.app.model.Province;

import android.content.Context;
import android.content.SharedPreferences;
import android.preference.PreferenceManager;
import android.text.TextUtils;
import android.util.Log;

public class Utility {
	/*
	 * 解析和处理服务器返回的升级数据
	 * */
	
	//线程 锁 ！！
	public synchronized static boolean handleProvinceResponse(CoolWeatherDB coolweatherDB,String response){
		       
		if(!TextUtils.isEmpty(response)){
		        	String[] allProvince = response.split(",");
		        	if(allProvince!= null&&allProvince.length>0){
		        		for(String p : allProvince){
		        			String[] array = p.split("\\|");
		        			Province province = new Province();
		        			province.setProvinceCode(array[0]);
		        			province.setProvinceName(array[1]);
		        			//将解析出来的数据存储到Province表
		        			coolweatherDB.saveProvince(province);
		        		}
		        		return true;
		        	}
		        	
		        }
		
		       return false;
	}
	
	/*
	 * 解析和处理服务器返回市的数据
	 * */
	public static boolean handleCitiesResponse(CoolWeatherDB coolWeatherDB,String response,int provinceId){
		if(!TextUtils.isEmpty(response)){
				Log.e("handleCitiesResponse", response);
			String [] allCities = response.split(",");
			if(allCities!=null&&allCities.length>0){
				for(String c :allCities){
					 String[] array = c.split("\\|");
					 City city = new City();
					 city.setCityCode(array[0]);
					 city.setCityName(array[1]);
					 city.setProivnceId(provinceId);
					 //将解析出来的数据存储在City表
					 coolWeatherDB.saveCity(city);
					 
				}
				return true;
			}
		}
		return false;
	}
	
	/*
	 * 将解析和处理服务器返回的县级数据
	 * */
	public static boolean handleCountiesResponse(CoolWeatherDB coolWeatherDB,String response,int cityId){
		if(!TextUtils.isEmpty(response)){
			String[] allCounties = response.split(",");
			if(allCounties!=null&&allCounties.length>0){
				 for(String c: allCounties){
					 String [] array = c.split("\\|");
					 County  county = new County();
					 county.setCountyCode(array[0]);
					 county.setCountyName(array[1]);
					 county.setCityId(cityId);
					 //将解析出来的数据存储到County表
					 coolWeatherDB.saveCounty(county);
				 }
				 return true;
			}
		}
		return false;
	}
	
	/*
	 * 解析服务器返回的JSON数据，并将解析出来的数据存储到本地
	 * */
	public static void handleWeatherResponse(Context context,String response){
		     try{
		    	 JSONObject jsonObject = new JSONObject(response);
		    	 JSONObject weatherInfo = jsonObject.getJSONObject("weatherinfo");
		    	 String cityName = weatherInfo.getString("city");
		    	 String weatherCode = weatherInfo.getString("cityid");
		    	 String temp1 = weatherInfo.getString("temp1");
		    	 String temp2 = weatherInfo.getString("temp2");
		    	 String weatherDesp = weatherInfo.getString("weather");
		    	 String publishTime = weatherInfo.getString("ptime");
		    	  Log.e("Utility", temp2+"U");
		    	 saveWeatherInfo(context,cityName,weatherCode,temp1,temp2,weatherDesp,publishTime);
		    	 
		     }catch(JSONException e){
		    	   e.printStackTrace();
		     }
	}

	private static void saveWeatherInfo(Context context, String cityName, String weatherCode, String temp1,
			String temp2, String weatherDesp, String publishTime) {
	     
		SimpleDateFormat  sdf= new SimpleDateFormat("yyyy年M月d日",Locale.CHINA);
		
		//getDefaultSharedPreferences(Context),获取 SharedPreferences 对象
		//自动使用应用程序包名作为前缀
		SharedPreferences.Editor editor = PreferenceManager.getDefaultSharedPreferences(context).edit();
		
		editor.putBoolean("city_selected", true);
		editor.putString("city_name", cityName);
		editor.putString("weather_code", weatherCode);
		editor.putString("temp1", temp1);
		editor.putString("temp2", temp2);
		editor.putString("weather_desp", weatherDesp);
		editor.putString("publish_time", publishTime);
		editor.putString("current_data", sdf.format(new Date()));
		Log.e("Utility", editor.putString("temp2", temp2)+"T");
		editor.commit();
		
				
		
	}
	

}
